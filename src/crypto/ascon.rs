    fn rand32(seed: &mut u64) -> u32 {
        *seed = seed
            .wrapping_mul(25_214_903_917_u64)
            .wrapping_add(11)
            & ((1u64 << 48) - 1);
        (*seed >> 16) as u32
    }

    fn rand_bytes_seeded_from_len(len: usize) -> Vec<u8> {
        let mut seed = len as u64;
        let mut out = vec![0u8; len];
        for byte in out.iter_mut() {
            *byte = rand32(&mut seed) as u8;
        }
        out
    }

    #[test]
    fn hash256_kat_vectors() {
        for (len, expected) in HASH_TESTVECS {
            let msg = rand_bytes_seeded_from_len(*len);
            let digest = hash256(&msg);
            if &digest != expected {
                panic!(
                    "len={len} got={} expected={}",
                    hex(&digest),
                    hex(expected)
                );
            }
        }
    }

    #[test]
    fn hash256_consolidated_vectors() {
        let data = rand_bytes_seeded_from_len(4096);
        let mut hasher = AsconHash256::new();
        for len in 0..=4096 {
            let digest = hash256(&data[..len]);
            hasher.update(&digest);
        }
        let consolidated = hasher.finalize();
        if consolidated != CONSOLIDATED {
            panic!(
                "consolidated got={} expected={}",
                hex(&consolidated),
                hex(&CONSOLIDATED)
            );
        }
    }

    fn hex(bytes: &[u8]) -> String {
        let mut out = String::with_capacity(bytes.len() * 2);
        for b in bytes {
            let _ = write!(out, "{:02x}", b);
        }
        out
    }
