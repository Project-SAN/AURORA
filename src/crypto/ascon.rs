//! Minimal Ascon-p[12] permutation and Ascon-Hash256 (no_std).

const ROUND_CONSTANTS: [u8; 12] = [
    0xf0, 0xe1, 0xd2, 0xc3, 0xb4, 0xa5, 0x96, 0x87, 0x78, 0x69, 0x5a, 0x4b,
];

#[inline]
fn rotr(x: u64, n: u32) -> u64 {
    (x >> n) | (x << (64 - n))
}

#[inline]
fn round(state: &mut [u64; 5], rc: u8) {
    // add round constant
    state[2] ^= rc as u64;

    // substitution layer
    state[0] ^= state[4];
    state[4] ^= state[3];
    state[2] ^= state[1];

    let t0 = (!state[0]) & state[1];
    let t1 = (!state[1]) & state[2];
    let t2 = (!state[2]) & state[3];
    let t3 = (!state[3]) & state[4];
    let t4 = (!state[4]) & state[0];

    state[0] ^= t1;
    state[1] ^= t2;
    state[2] ^= t3;
    state[3] ^= t4;
    state[4] ^= t0;

    state[1] ^= state[0];
    state[0] ^= state[4];
    state[3] ^= state[2];
    state[2] = !state[2];

    // linear diffusion
    state[0] ^= rotr(state[0], 19) ^ rotr(state[0], 28);
    state[1] ^= rotr(state[1], 61) ^ rotr(state[1], 39);
    state[2] ^= rotr(state[2], 1) ^ rotr(state[2], 6);
    state[3] ^= rotr(state[3], 10) ^ rotr(state[3], 17);
    state[4] ^= rotr(state[4], 7) ^ rotr(state[4], 41);
}

pub fn permute12(state: &mut [u64; 5]) {
    for &rc in ROUND_CONSTANTS.iter() {
        round(state, rc);
    }
}

const RATE_BYTES: usize = 8;
const HASH_IV: u64 = 0x0000_0801_00cc_0002;

/// Ascon-Hash256 (NIST SP 800-232) with a 64-bit rate and 256-bit output.
pub struct AsconHash256 {
    state: [u64; 5],
    buf: [u8; RATE_BYTES],
    pos: usize,
}

impl AsconHash256 {
    pub fn new() -> Self {
        let mut state = [0u64; 5];
        state[0] = HASH_IV;
        permute12(&mut state);
        Self {
            state,
            buf: [0u8; RATE_BYTES],
            pos: 0,
        }
    }

    pub fn update(&mut self, mut data: &[u8]) {
        if data.is_empty() {
            return;
        }

        if self.pos != 0 {
            let take = core::cmp::min(RATE_BYTES - self.pos, data.len());
            self.buf[self.pos..self.pos + take].copy_from_slice(&data[..take]);
            self.pos += take;
            data = &data[take..];
            if self.pos == RATE_BYTES {
                self.absorb_block();
            }
        }

        while data.len() >= RATE_BYTES {
            self.buf.copy_from_slice(&data[..RATE_BYTES]);
            data = &data[RATE_BYTES..];
            self.absorb_block();
        }

        if !data.is_empty() {
            self.buf[..data.len()].copy_from_slice(data);
            self.pos = data.len();
        }
    }

    pub fn finalize(mut self) -> [u8; 32] {
        let mut block = [0u8; RATE_BYTES];
        if self.pos != 0 {
            block[..self.pos].copy_from_slice(&self.buf[..self.pos]);
        }
        block[self.pos] ^= 0x01;
        self.state[0] ^= u64::from_le_bytes(block);
        permute12(&mut self.state);

        let mut out = [0u8; 32];
        for chunk in out.chunks_exact_mut(RATE_BYTES).take(3) {
            chunk.copy_from_slice(&self.state[0].to_le_bytes());
            permute12(&mut self.state);
        }
        out[24..32].copy_from_slice(&self.state[0].to_le_bytes());
        out
    }

    fn absorb_block(&mut self) {
        let word = u64::from_le_bytes(self.buf);
        self.state[0] ^= word;
        permute12(&mut self.state);
        self.buf.fill(0);
        self.pos = 0;
    }
}

pub fn hash256(data: &[u8]) -> [u8; 32] {
    let mut hasher = AsconHash256::new();
    hasher.update(data);
    hasher.finalize()
}

